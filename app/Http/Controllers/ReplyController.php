    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
        $request->validate([
            'name' => 'required|string',
            'email' =>  'required|email',
            'comment' => 'required',
            'company'  => 'nullable',
            'phone'  => 'nullable',
            'company'  => 'nullable',
        ]);
        Reply::create([
            'name'      => $request->name,
            'content'   => $request->comment,
            'email'     => $request->email,
            'phone'     => $request->phone,
            'company'   => $request->company,
            'save'      => $request->save,
            'comment_id'=> decrypt($request->comment_id),
            'blog_id'   => decrypt(session('post')),
            'user_id'   => Auth::id(),
        ]);
        $request->session()->forget('comment', 'post');
        return back();
    }

